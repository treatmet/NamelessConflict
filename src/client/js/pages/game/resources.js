function playGrenadeExplosionSfx(x, y){ //playExplosionSfx
	var maxDist = 1500;
	var minDist = 750;

	if (!mute){
		var explosion_sounds = ["Explosion1", "Explosion2", "Explosion3", "Explosion4"];
		var randy = Math.floor(Math.random() * explosion_sounds.length);
		var srandy = randy;
		do {
			if (!eval(`sfx.${explosion_sounds[srandy]}.playing()`)) {
				sfxRangedLoud(explosion_sounds[srandy], x, y, maxDist, minDist);;
				break;
			}
			console.log(srandy);
			srandy++;
			srandy = srandy % explosion_sounds.length;
		} while (randy != srandy);
	}
}

